const os = require('os');


// imp node OS modules ;
// these are available on official website of node
// from os modules you can know about knowledge of operating system

// Host name of Computer
console.log(os.hostname());

// Free memory of computer
console.log(os.freemem());

// type of Computer
console.log(os.type());

// version of computer
console.log(os.version());

// on-time of computer
console.log(os.uptime());

// home-directory of computer
console.log(os.homedir());

// user info of computer
console.log(os.userInfo());

// platform of computer
console.log(os.platform());